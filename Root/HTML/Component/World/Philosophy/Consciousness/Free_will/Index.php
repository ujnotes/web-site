	<h3>What is free will?</h3>
	<p>
		Free will is the ability to choose a course of action independent of prior events.<br>
		E.g. imagine a series of dominos. What happens to the next domino is determined, other than the inherent static state of that system - like the mass and position of the dominos, entirely by the action of the domino just beside it.<br>
		If we have all the required information of any moment, we can predict exactly what will happen next.<br>
		But say, if a domino was free to decide and choose on its own, what it would do next amongst a set of more than one possibilities - independent of the events before it, we would never be able to deterministically assert; and thus it would be said to have free will.<br>
	</p>
	
	<h3>Do we have free will?</h3>
	<p>
		Are we 'free', to think and decide and choose?
		Or is it that we are merely acting out the choices that have already been made.
	</p>
	<p>
