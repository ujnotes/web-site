		As such, the numbers they generate are in fact pseudo-random numbers.<br>
		It turns out we ourselves could be - like those dumb calculators.<br>
		Another important example is of the answers to questions like one's favorite dish or colour or car.<br>
		A subject can 'only' answer with a case - that he has prior to that point, heard or seen.
	</p>
	<p>
		The past DETERMINES the future... We only observe it, we only observe the act of choosing - but the choice perhaps has already been made.<br>
		The issue here is that our brain itself is part of this world - and functions on the parameters of the world.<br>
		So while our act of observing ourselves making a choice might be 'real', the whole 'scene' though - could simply be predetermined and preordained.
	</p>
