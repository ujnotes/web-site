	<h3>What are thoughts?</h3>
	<p>
		A thought is a virtual partial simulation of reality. Evolution devised it for survival.<br>
		E.g. a sight of predator charging towards oneself triggers a 'thought' of being eaten alive by it next - thereby making the individual act in order to save its life.<br>
	</p>
	<h3>What is self-awareness?</h3>
	<p>
		Thoughts that are aware of its own existence - the simulator, - can be said to be "self-aware".<br>
		<br>
		As such a self-aware system has a sense of the 'I'. In other words, it can simulate itself.<br>
		E.g. a self-driving car's auto pilot software will include itself in the drive simulation. Besides its environment, it will have knowledge about its own extents and position and characteristics.<br>
		If it also knows that the one running the simulation is the same as its image in the simulation then it could be said to be self-aware.
	</p>
	<h3>What is consciousness?</h3>
	<p>
		Consciousness is the ability to be able to think - what to think. A conscious system is one that is self-aware and self-directing in that it can select what to think upon.<br>
		As such, it does not take direct instructions as to what to think about - from an external agency. It is the 'master' program, tasked with the role of managing thoughts and routing information and actions.
	</p>
